/**
 * @brief return the required data length for different ACPI command
 *
 * @param command ACPI command
 * @retval length
 */
static uint8_t smchost_req_length(uint8_t command)
{
	switch (command) {
	case SMCHOST_PLN_CONFIG:
	case SMCHOST_CS_LOW_PWR_MODE_SET:
	case SMCHOST_ACPI_READ:
	case SMCHOST_READ_ACPI_SPACE:
	case SMCHOST_HID_BTN_SCI_CONTROL:
#if CONFIG_THERMAL_MANAGEMENT
	case SMCHOST_BIOS_FAN_CONTROL:
	case SMCHOST_SET_SHDWN_THRESHOLD:
#endif
		return 1;

	case SMCHOST_ACPI_WRITE:
	case SMCHOST_WRITE_ACPI_SPACE:
		return 2;

	default:
		return 0;
	}
}

/**
 * @brief ACPI port 666/662 command handler
 *
 * @param command ACPI command
 */
static void smchost_cmd_handler(uint8_t command)
{
	switch (command) {

	/* Handlers for commands 80h to 8Fh */
	case SMCHOST_ACPI_READ:
		acpi_read_ec();
		break;
	case SMCHOST_ACPI_WRITE:
		acpi_write_ec();
		break;
	case SMCHOST_ACPI_BURST_MODE:
		acpi_burst_ec();
		break;
	case SMCHOST_ACPI_NORMAL_MODE:
		acpi_normal_ec();
		break;
	case SMCHOST_ACPI_QUERY:
		acpi_query_ec();
		break;
	case SMCHOST_UCSI_WRITE_DONE:
#if CONFIG_USBC_UCSI_TUNNEL
		app_ucsi_tunnel_hostWriteDone();
#endif
		break;
	case SMCHOST_UCSI_READ_DONE:
#if CONFIG_USBC_UCSI_TUNNEL
		app_ucsi_tunnel_hostReadDone();
#endif
		break;
	case SMCHOST_ACPI_HOST_ENABLE:
		ACPI_EnableHostNotify();
		break;
	case SMCHOST_ACPI_HOST_DISABLE:
		ACPI_DisableHostNotify();
		break;
	default:
		host_cmd_default(command);
		break;
	}
}