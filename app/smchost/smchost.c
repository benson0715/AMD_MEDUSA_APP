/**
 * @brief Detect the acpi input buffer full status, and return the acpi type.
 */
static uint8_t smchost_acpi_ibf_get(void)
{
	if (acpi_get_flag(ACPI_EC_0, ACPI_FLAG_IBF)) {
		return ESPIHUB_ACPI_PUBLIC;
	}

	if (acpi_get_flag(ACPI_EC_1, ACPI_FLAG_IBF)) {
		return ESPIHUB_ACPI_PRIVATE_0;
	}

	return MAX_ACPI_HANDLERS;
}

